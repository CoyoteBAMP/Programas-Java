public class MultiploDeTres
{
   public static void main(String[] args)
   {
      int numero=15;
      int residuo;
      residuo=numero%3;
      if (residuo==0)
      {
         System.out.println("El numero es multiplo de tres");
      }
      else
      {
         System.out.println("El numero no es multiplo de tres");
      }
   }
}