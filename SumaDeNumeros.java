public class SumaDeNumeros
{
   public static void main(String[] args)
   {
      int numero1=5;
      int numero2=1;
      int resultado;
      resultado=numero1+numero2;
      if (resultado>=5)
      {
         System.out.println("El resultado es mayor a 5");
      }
      else
      {
         System.out.println("El resultado es menor a 5");
      }
   }
} 